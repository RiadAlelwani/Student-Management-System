package infrastructure;

/**
 * هذه الواجهة تحدد مستمعًا للتغييرات في قائمة الطلاب.
 * الفئات التي تنفذ هذه الواجهة يجب أن تحدد ما يحدث
 * عند تعديل قائمة الطلاب.
 */
public interface StudentObserver {
    /**
     * يتم استدعاء هذه الطريقة عند تغيير قائمة الطلاب.
     * يجب على الفئات المنفذة تحديد المنطق للاستجابة
     * لمثل هذه التغييرات (مثل تحديث مكون واجهة المستخدم).
     */
    void onStudentListChanged();
}